package main

import "fmt"

func main() {
    a := 10
    b := 3

    fmt.Println("Adição:", a + b)       // 13
    fmt.Println("Subtração:", a - b)    // 7
    fmt.Println("Multiplicação:", a * b) // 30
    fmt.Println("Divisão:", a / b)      // 3 (inteiro)
    fmt.Println("Módulo:", a % b)       // 1
}